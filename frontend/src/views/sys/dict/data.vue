<template>
  <BasicLayout>
    <template #wrapper>
      <el-card class="box-card">
        <el-form ref="queryForm" :model="queryParams" :inline="true">
          <el-form-item label="字典名称" prop="key">
            <el-select v-model="queryParams.key" size="small">
              <el-option
                v-for="item in typeOptions"
                :key="item.id"
                :label="item.name"
                :value="item.key"
              />
            </el-select>
          </el-form-item>
          <el-form-item label="字典标签" prop="label">
            <el-input
              v-model="queryParams.label"
              placeholder="请输入字典标签"
              clearable
              size="small"
              @keyup.enter.native="handleQuery"
            />
          </el-form-item>
          <el-form-item label="状态" prop="status">
            <dict-select v-model="queryParams.status" :options="statusOptions" placeholder="数据状态" />
          </el-form-item>
          <el-form-item>
            <el-button type="primary" icon="el-icon-search" size="mini" @click="handleQuery">搜索</el-button>
            <el-button icon="el-icon-refresh" size="mini" @click="resetQuery">重置</el-button>
          </el-form-item>
        </el-form>

        <el-row :gutter="10" class="mb8">
          <el-col :span="1.5">
            <el-button
              v-permisaction="['sys:dictdata:create']"
              type="primary"
              icon="el-icon-plus"
              size="mini"
              @click="handleAdd"
            >新增</el-button>
          </el-col>
          <el-col :span="1.5">
            <el-button
              v-permisaction="['sys:dictdata:update']"
              type="success"
              icon="el-icon-edit"
              size="mini"
              :disabled="single"
              @click="handleUpdate"
            >修改</el-button>
          </el-col>
          <el-col :span="1.5">
            <el-button
              v-permisaction="['sys:dictdata:delete']"
              type="danger"
              icon="el-icon-delete"
              size="mini"
              :disabled="multiple"
              @click="handleDelete"
            >删除</el-button>
          </el-col>
        </el-row>

        <el-table v-loading="loading" :data="records" @selection-change="handleSelectionChange">
          <el-table-column type="selection" width="55" align="center" />
          <el-table-column label="字典编码" width="80" align="center" prop="id" />
          <el-table-column label="字典标签" align="center" prop="label" />
          <el-table-column label="字典键值" align="center" prop="value" />
          <el-table-column label="字典排序" align="center" prop="sort" />
          <el-table-column label="状态" align="center">
            <template slot-scope="scope">
              <status-tag :status="scope.row.status" />
            </template>
          </el-table-column>
          <el-table-column label="备注" align="center" prop="remark" :show-overflow-tooltip="true" />
          <el-table-column label="创建时间" align="cneter" prop="createdAt" width="180" />
          <el-table-column label="操作" align="center" class-name="small-padding fixed-width">
            <template slot-scope="scope">
              <el-button
                v-permisaction="['sys:dictdata:update']"
                size="mini"
                type="text"
                icon="el-icon-edit"
                @click="handleUpdate(scope.row)"
              >修改</el-button>
              <el-button
                v-permisaction="['sys:dictdata:delete']"
                size="mini"
                type="text"
                icon="el-icon-delete"
                @click="handleDelete(scope.row)"
              >删除</el-button>
            </template>
          </el-table-column>
        </el-table>

        <pagination
          v-show="total>0"
          :total="total"
          :page.sync="queryParams.page"
          :limit.sync="queryParams.limit"
          @pagination="getList"
        />

        <!-- 添加或修改参数配置对话框 -->
        <el-dialog :title="title" :visible.sync="open" :close-on-click-modal="false" width="500px">
          <el-form ref="form" :model="form" :rules="rules" label-width="80px">
            <el-form-item label="字典类型" prop="dictType">
              <el-input v-model="form.dictType" :disabled="true" />
            </el-form-item>
            <el-form-item label="数据标签" prop="label">
              <el-input v-model="form.label" placeholder="请输入数据标签" :disabled="isEdit" />
            </el-form-item>
            <el-form-item label="数据键值" prop="value">
              <el-input v-model="form.value" placeholder="请输入数据键值" :disabled="isEdit" />
            </el-form-item>
            <el-form-item label="显示排序" prop="sort">
              <el-input-number v-model="form.sort" controls-position="right" :min="0" />
            </el-form-item>
            <el-form-item label="状态" prop="status">
              <status-radio-group v-model="form.status" />
            </el-form-item>
            <el-form-item label="备注" prop="remark">
              <el-input v-model="form.remark" type="textarea" placeholder="请输入内容" />
            </el-form-item>
          </el-form>
          <div slot="footer" class="dialog-footer">
            <el-button type="primary" @click="submitForm">确 定</el-button>
            <el-button @click="cancel">取 消</el-button>
          </div>
        </el-dialog>
      </el-card>
    </template>
  </BasicLayout>
</template>
<script>
import { mapGetters } from 'vuex'
import { listData, getData, updateData, createData, deleteData } from '@/api/sys/dict/data'
import { listType, getType } from '@/api/sys/dict/type'
import StatusTag from '@/components/StatusTag'
import StatusRadioGroup from '@/components/StatusRadioGroup'
import DictSelect from '@/components/DictSelect'

export default {
  name: 'Data',
  components: {
    StatusTag,
    StatusRadioGroup,
    DictSelect
  },
  data() {
    return {
      loading: true,
      ids: [],
      single: true,
      multiple: true,
      total: 0,
      records: [],
      dictId: undefined,
      dictType: '',
      title: '',
      // 是否显示弹出层
      open: false,
      isEdit: false,
      typeOptions: [],

      queryParams: {
        page: 1,
        limit: 10,
        label: undefined,
        key: undefined,
        status: undefined
      },

      form: {},
      rules: {
        label: [
          { required: true, message: '数据标签不能为空', trigger: 'blur' }
        ],
        value: [
          { required: true, message: '数据键值不能为空', trigger: 'blur' }
        ],
        sort: [
          { required: true, message: '数据顺序不能为空', trigger: 'blur' }
        ]
      }
    }
  },
  computed: {
    ...mapGetters(['statusOptions'])
  },
  created() {
    const dictId = this.$route.params && this.$route.params.dictId
    this.dictId = dictId
    this.form.dictId = dictId
    this.getType(dictId)
    this.getTypeList()
  },
  methods: {
    // 表单重置
    reset() {
      this.form = {
        dictId: this.dictId,
        dictType: this.dictType,
        label: undefined,
        value: undefined,
        sort: 0,
        status: 1,
        remark: undefined
      }
      this.resetForm('form')
    },
    getType(dictId) {
      getType(dictId).then(resp => {
        const { data } = resp
        this.queryParams.key = data.key
        this.dictType = data.key
        this.form.dictType = data.key
        this.getList()
      })
    },
    getTypeList() {
      listType().then(resp => {
        this.typeOptions = resp.data.records
      })
    },
    getList() {
      this.loading = true
      listData(this.queryParams).then(resp => {
        const { data } = resp
        this.records = data.records
        this.total = data.total
        this.loading = false
      })
    },
    handleQuery() {
      this.queryParams.page = 1
      this.getList()
    },
    resetQuery() {
      this.resetForm('queryForm')
      this.handleQuery()
    },
    // 取消按钮
    cancel() {
      this.open = false
      this.reset()
    },
    /** 新增按钮操作 */
    handleAdd() {
      this.reset()
      this.open = true
      this.title = '添加字典类型'
      this.isEdit = false
    },
    /** 修改按钮操作 */
    handleUpdate(row) {
      this.reset()
      const id = row.id || this.ids
      getData(id).then(response => {
        const { data } = response
        this.form = data
        this.form.dictType = this.dictType
        this.open = true
        this.title = '修改字典数据'
        this.isEdit = true
      })
    },
    /** 提交按钮 */
    submitForm: function() {
      this.$refs['form'].validate(valid => {
        if (valid) {
          if (this.form.id !== undefined) {
            updateData(this.form.id, this.form).then(response => {
              if (response.success) {
                this.msgSuccess('修改成功')
                this.open = false
                this.getList()
              } else {
                this.msgError(response.msg)
              }
            })
          } else {
            createData(this.form).then(response => {
              if (response.success) {
                this.msgSuccess('新增成功')
                this.open = false
                this.getList()
              } else {
                this.msgError(response.msg)
              }
            })
          }
        }
      })
    },
    /** 删除按钮操作 */
    handleDelete(row) {
      const ids = row.id || this.ids
      this.$confirm(`是否确认删除字典编码为”${ids}“的数据项？`, '警告', {
        confirmButtonText: '确定',
        cancelButtonText: '取消',
        type: 'warning'
      }).then(function() {
        return deleteData(ids)
      }).then(() => {
        this.getList()
        this.msgSuccess('删除成功')
      }).catch(() => {})
    },
    handleSelectionChange(selection) {
      this.ids = selection.map(item => item.id)
      this.single = selection.length !== 1
      this.multiple = !selection.length
    }
  }
}
</script>
